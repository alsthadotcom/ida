export const getIdeaById = (id: number) => {
    return IDEAS_DATA.find(idea => idea.id === id);
};

// Secure slug-based lookup
export const getIdeaBySlug = (slug: string) => {
    // Sanitize slug to prevent injection attacks
    const sanitizedSlug = slug.toLowerCase().replace(/[^a-z0-9-]/g, '');
    return IDEAS_DATA.find(idea => idea.slug === sanitizedSlug);
};
